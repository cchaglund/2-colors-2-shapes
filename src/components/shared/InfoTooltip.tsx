import { useState, type ReactNode } from 'react';
import cn from "classnames";
import {
  useFloating,
  useHover,
  useInteractions,
  offset,
  flip,
  shift,
  FloatingPortal,
} from '@floating-ui/react';

interface TooltipProps {
  text: string;
  children: ReactNode;
  capitalize?: boolean;
}

export function Tooltip({ text, children, capitalize }: TooltipProps) {
  const [isOpen, setIsOpen] = useState(false);

  const { refs, floatingStyles, context } = useFloating({
    open: isOpen,
    onOpenChange: setIsOpen,
    middleware: [offset(8), flip(), shift()],
    placement: 'top',
  });

  const hover = useHover(context);
  const { getReferenceProps, getFloatingProps } = useInteractions([hover]);

  // These are callback refs from floating-ui, safe to use during render
  const setReference = refs.setReference;
  const setFloating = refs.setFloating;

  return (
    <>
      <div
        // eslint-disable-next-line react-hooks/refs -- callback ref from floating-ui
        ref={setReference}
        {...getReferenceProps()}
        className="inline-block h-full"
      >
        {children}
      </div>
      {isOpen && (
        <FloatingPortal>
          <div
            // eslint-disable-next-line react-hooks/refs -- callback ref from floating-ui
            ref={setFloating}
            style={floatingStyles}
            {...getFloatingProps()}
            className={cn(
              "max-w-xs px-2.5 py-1.5 text-(--text-xs) text-(--color-bg-primary) bg-(--color-text-primary) rounded-(--radius-md) z-50",
              { "capitalize": capitalize }
            )}
          >
            {text}
          </div>
        </FloatingPortal>
      )}
    </>
  );
}

export function InfoTooltip({ text }: { text: string }) {
  return (
    <Tooltip text={text}>
      <span
        className="lowercase align-middle inline-flex items-center justify-center w-4 h-4 ml-1.5 text-(--text-xs) rounded-(--radius-pill) bg-(--color-border) text-(--color-text-tertiary) cursor-help hover:bg-(--color-accent) hover:text-(--color-accent-text) transition-colors"
        aria-label="More information"
      >
        i
      </span>
    </Tooltip>
  );
}
